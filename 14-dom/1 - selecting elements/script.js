// getElementById() (element) - Seleciona elementos a partir da ID
const element = document.getElementById("blog-title")
console.log(element)

// getElementByClassName() (HTMLCollection) - Seleciona elementos a partir dos nomes das classes
const element2 = document.getElementsByClassName("one")
console.log(element2)

// getElementByTagName() (HTMLCollection) - Seleciona Elementos a partir dos nomes das tags
const element3 = document.getElementsByClassName("meta")
console.log(element3)

// querySelector() (element)
const element4 = document.querySelector("[src]")
console.log(element4)

// querySelectorAll() (NodeList)
const element5 = document.querySelectorAll("#one")
console.log(element5)

